import numpy as np

def predict_machine(vibration, acoustic, temperature, current,
                    IMF_1, IMF_2, IMF_3):

    input_data = np.array([[vibration,
                            acoustic,
                            temperature,
                            current,
                            IMF_1,
                            IMF_2,
                            IMF_3]])

    prediction = best_model.predict(input_data)

    if prediction[0] == 0:
        return "🟢 Normal Machine"
    else:
        return "🔴 Failure Risk Detected"

